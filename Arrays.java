public class Arrays{
	public static void main(String[] args) {
		/* Storing an Array we add brackets to the variable type followed by the variable
		 * we assign the elements to the variable in curly braces 
		 * Example
		 * variable name = {};
		 * Variable type []
		 *  
		 */
		
		// Variable initialization
		String[] cars = {"Volvo", "Bens", "BMW"};
		
		// Output
		System.out.println(cars[0]); // Output: Volvo
		
		
		//integer arrays
		int[] numbers = {1, 2, 3, 4, 5};
		
		System.out.println(numbers[0]);
	}
}