
// Java operators

public class Operators{
	public static void main(String[] args) {
		   	
    	/* Operators - are used to perform operations on values and variables 
    	 * Example - the + operator used to add together values or add together variables 
    	 * 
    	 * The different groups of operators:
    	 * Arithmetic operators
    	 * Assignment operators
    	 * Comparison operators 
    	 * Logical operators
    	 * Bitwise operators  
    	 */
    	
    	
    	/* Arithmetic Operators:
    	 * these are used to perform common mathematical operations
    	  */
    	
    	// Addition 
    	int x = 2, y = 4;
    	int add = x + y;
    	
    	System.out.println(add); // Output: 6
    	
    	// Subtraction 
    	int a = 4, b  = 2;
    	int sub = a - b;
    	
    	System.out.println(sub); // Output: 2
    	
    	
    	// Multiplication 
    	int c = 10, d = 10;
    	int multi = c * d;
    	
    	System.out.println(multi); // Output: 100
    	
    	// Division
    	int e = 10, f = 2;
    	int div = e / f;
    	
    	System.out.println(div); // Output: 5
    	
    	// Modulus 
    	int g = 16, h = 3;
    	int modu = g % h;
    	
    	System.out.println(modu); // Output: 1
    	
    	// Increment and Decrement
    	int s = 1, n = 4;
    	int incre = ++s, decre = --n;
    	    	
    	System.out.println("Value: "+ incre); // Output Value: 2
    	System.out.println("Value: "+ decre); // Output Value: 3
    	
    	    	    	
	}
}